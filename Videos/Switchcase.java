class Switchcase{
    public static void main(String[] args) {
        int option = 1;
        switch (option) {
            case 1:
                System.out.println("option 1");
                break;
            case 2:
                System.out.println("option 2");
                break;
            case 3:
                System.out.println("option 3");
                break;
            default:
                System.out.println("not valid");
        }
    }
}